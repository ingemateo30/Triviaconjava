    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String rta = null;
        int nivelsumar=Integer.parseInt(txtnivel.getText());
        if(rta1.isSelected())
        {
            rta = rta1.getText();  
        }else 
            if(rta2.isSelected())
            {
               rta = rta2.getText();
            }else 
                if(rta3.isSelected())
                {
                   rta = rta3.getText();
                }else 
                     if(rta4.isSelected())
                     {
                        rta = rta4.getText();
                     }
        for (pregunta p : a) {
            
            if(rta == null)
            {
                verificarvacios();
            }else
                if(p.getRespuestacorrecta().equals(rta)){
                    JOptionPane.showMessageDialog(this, "respuesta correcta");
                    validar_puntaje();
                    txtpuntos.setText(String.valueOf(puntaje));
                    nivelsumar ++;
                    txtnivel.setText(String.valueOf(nivelsumar));
                    preguntas();
                    deseleccionar();
                    validar_final();
                }else{
                    validar2();
                    JOptionPane.showMessageDialog(this, "respuesta incorrecta,la respuesta correcta es" + p.getRespuestacorrecta());
                    dispose();
                    String nombres=usuario.getText();
                     perdio CrUsu = new perdio();
                     CrUsu.Mostrar(nombres,puntaje);
                     CrUsu.setVisible(true);
                }
             
        }
    }//GEN-LAST:event_jButton1ActionPerformed

