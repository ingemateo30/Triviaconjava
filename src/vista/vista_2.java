    public void Mostrar(String Usuario,String nivel,String categoria)
    /*metodo para mostrar los datos respectivos de la persona que esta jugando*/
    {
        String puntajes=String.valueOf(puntaje);
        usuario.setText(Usuario);
        usuario.setEnabled(false);
        txtpuntos.setText(puntajes);
        txtpuntos.setEnabled(false);
        txtcategoria.setText(categoria);
        txtcategoria.setEnabled(false);
        txtnivel.setText(nivel);
        txtnivel.setEnabled(false);
    }
    
    public void preguntas()
    /*metodo para mostrar la preguntas en el formulario*/
    {
    String categorias= txtcategoria.getText();
    int nivel= Integer.parseInt(txtnivel.getText());
    this.controlador=new controlador();
    this.c=controlador.sacarcodigo(categorias);
    for (categoria categorias1 : c) 
        {
             String plan = String.valueOf(categorias1.getId());
             this.txtid.setText(plan);
        }
    int codigo= Integer.parseInt(txtid.getText());
    this.a=controlador.buscarpreguntas(codigo, nivel);
    for (pregunta p : a) {
             String pregunta = String.valueOf(p.getPregunta());
             String rta1 = String.valueOf(p.getRespuesta1());
             String rta2 = String.valueOf(p.getRespuesta2());
             String rta3 = String.valueOf(p.getRespuesta3());
             String rta4 = String.valueOf(p.getRespuestacorrecta());
             this.txtpregunta.setText(" " + pregunta);
             this.rta1.setText(rta1);
             this.rta2.setText(rta2);
             this.rta3.setText(rta3);
             this.rta4.setText(rta4);
             
        }
    }
    
    void deseleccionar()
    /*metodo para limpiar los radiobuttons al pasar a la siguiente pregunta*/
    {
    rta1.setSelected(false);
    rta2.setSelected(false);
    rta3.setSelected(false);
    rta4.setSelected(false);
    
    }
    
    public void verificarvacios()
    /*metodo para verificar que se tenga que escoger una respuesta*/
    {
       if((rta1.isSelected()==false&& rta2.isSelected()==false&& rta3.isSelected()==false&& rta4.isSelected()==false))
       {
          JOptionPane.showMessageDialog(this, "escoja 1 respuesta");
       } 
    }
   
    void validar_final()
    /*metodo para verificar que el juego acabo*/
    {
        int nivel=Integer.parseInt(txtnivel.getText());
        if(nivel>= 6) 
        {
            String nombre = usuario.getText();
            String puntaje = txtpuntos.getText();
            String categoria = txtcategoria.getText();
            Usuarios user = new Usuarios(nombre, puntaje,categoria);
            controlador controlador= new controlador();
            controlador.crearregistro(user);
            dispose();
            ganador CrUsu = new ganador();
            CrUsu.setVisible(true);
        }
    }
    void validar2()
    /*metodo para verificar cuando una persona perdio y se le deben guardar 
    los registros */
    {
        
        String nombre = usuario.getText();
        String puntaje = txtpuntos.getText();
        String categoria = txtcategoria.getText();
        Usuarios user = new Usuarios(nombre, puntaje,categoria);
        controlador controlador= new controlador();
        controlador.crearregistro2(user);
        
    }
    void validar_puntaje(){
        int nivel= Integer.parseInt(txtnivel.getText());
        switch (nivel)
        {
            case 1:
                puntaje=puntaje+100;
                break;
            case 2:
                puntaje=puntaje+200;
                break;
            case 3:
                puntaje=puntaje+300;
                break;
                 case 4:
                puntaje=puntaje+400;
                break;
                 case 5:
                puntaje=puntaje+500;
                break;
            default:
                break;
        }
    }
